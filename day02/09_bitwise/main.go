package main

import "fmt"

func main() {
	// [비트 연산자: 컴퓨터의 언어로 대화하기]
	// 비트 연산은 숫자를 0과 1의 조합(2진수)으로 보고 계산하는 아주 미세한 작업입니다.
	// ※ 비전공자 Tip: 지금은 "이런 게 있구나" 하고 구경만 하셔도 충분합니다! 나중에 정말 필요할 때 다시 봐도 늦지 않아요.
	
	var a int = 12 // 2진수로 1100
	var b int = 10 // 2진수로 1010

	fmt.Println("--- 비트 연산자 (2진수 계산기) ---")
	fmt.Printf("a = %d \t (2진수: %04b)\n", a, a)
	fmt.Printf("b = %d \t (2진수: %04b)\n", b, b)

	fmt.Println("\n--- 1. 비트 AND (&) ---")
	// 둘 다 1일 때만 1을 줍니다. (까다로운 조건)
	// 1100 & 1010 = 1000 (8)
	fmt.Printf("a & b \t = %d \t (둘 다 1인 곳만 1)\n", a&b)

	fmt.Println("\n--- 2. 비트 OR (|) ---")
	// 둘 중 하나라도 1이면 1을 줍니다. (너그러운 조건)
	// 1100 | 1010 = 1110 (14)
	fmt.Printf("a | b \t = %d \t (하나라도 1이면 1)\n", a|b)

	fmt.Println("\n--- 3. 비트 XOR (^) ---")
	// 서로 다를 때만 1을 줍니다. (청개구리 조건)
	// 1100 ^ 1010 = 0110 (6)
	fmt.Printf("a ^ b \t = %d \t (서로 다를 때만 1)\n", a^b)

	fmt.Println("\n--- 4. 비트 클리어 (&^) ---")
	// a 비트 중에서 b 비트가 1인 자리를 0으로 청소해버립니다.
	// 1100 &^ 1010 = 0100 (4)
	fmt.Printf("a &^ b \t = %d \t (b가 1인 자리를 0으로)\n", a&^b)

	fmt.Println("\n--- 5. 비트 시프트 (<<, >>) ---")
	// 비트들을 왼쪽이나 오른쪽으로 밀어버립니다.
	var c int = 1 // 0001
	
	// << 2 : 왼쪽으로 2칸 밀기 (0001 -> 0100)
	// 왼쪽으로 한 칸 밀 때마다 숫자가 2배씩 커집니다.
	fmt.Printf("1 << 2 \t = %d \t (왼쪽으로 2칸 밀기)\n", c<<2)
	
	var d int = 8 // 1000
	// >> 2 : 오른쪽으로 2칸 밀기 (1000 -> 0010)
	// 오른쪽으로 한 칸 밀 때마다 숫자가 절반(1/2)으로 줄어듭니다.
	fmt.Printf("8 >> 2 \t = %d \t (오른쪽으로 2칸 밀기)\n", d>>2)
}

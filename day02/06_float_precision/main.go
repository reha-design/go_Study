package main

import "fmt"

func main() {
	// [1. 실수 타입: float32 vs float64]
	// 실수는 소수점이 있는 숫자입니다.
	// float64가 float32보다 더 넓은 공간을 써서 더 정밀하게 숫자를 보관합니다.
	// 그래서 Go에서는 특별한 이유가 없다면 float64를 기본으로 씁니다.
	var f1 float32 = 0.1234567890123456789
	var f2 float64 = 0.1234567890123456789

	fmt.Println("1. 정밀도 비교 (누가 더 정확할까?):")
	fmt.Println("   float32 (작은 바구니):", f1) // 소수점 아래 약 7자리 이후로는 값이 틀어짐
	fmt.Println("   float64 (큰 바구니):", f2)   // 소수점 아래 약 15자리까지 정확

	// [2. 지수 표기법: e 사용하기]
	// 아주 큰 숫자(0이 많은 숫자)나 아주 작은 숫자를 짧게 쓸 때 사용합니다.
	var e1 float64 = 1.23e4  // 1.23 * 10,000 = 12300
	var e2 float64 = 1.23e-2 // 1.23 * 0.01 = 0.0123

	fmt.Println("\n2. 지수 표기법 (큰 수/작은 수 표현):")
	fmt.Printf("   1.23e4  (1.23만) : %v\n", e1)
	fmt.Printf("   1.23e-2 (0.0123) : %v\n", e2)

	// [3. 소수점 자리수 직접 지정하기]
	// 출력할 때 "나는 소수점 2자리까지만 보여줘!"라고 정할 수 있습니다.
	fmt.Println("\n3. 예쁘게 출력하기 (포맷팅):")
	fmt.Printf("   소수점 2자리까지: %.2f\n", f2) // .2f -> 소수점 아래 2자리

	// [4. ⭐️매우 중요: 컴퓨터는 실수를 완벽히 계산 못 함]
	// 컴퓨터는 모든 숫자를 0과 1(이진수)로 생각합니다. 
	// 우리가 쓰는 0.1이나 0.2를 이진수로 바꾸면 무한소수가 되어버려 끝부분이 아주 조금씩 잘립니다.
	fmt.Println("\n4. 실수의 계산 오차 (컴퓨터의 한계):")
	var a float64 = 0.1
	var b float64 = 0.2
	fmt.Printf("   0.1 + 0.2 = %v\n", a+b) 
	// 결과: 0.30000000000000004 (0.3이 아닙니다!)

	// [5. 오차를 해결하는 세 가지 요령]
	fmt.Println("\n5. 이 오차를 어떻게 피할까?")

	// 방법 1: 그냥 반올림해서 보여주기 (눈속임)
	// - 보여줄 때만 소수점 자리를 끊어서 오차를 안 보이게 합니다.
	fmt.Printf("   [방법1] %.1f + %.1f = %.1f  (화면에만 0.3으로 출력)\n", a, b, a+b)

	// 방법 2: 엡실론(Epsilon) 비교
	// - "두 값의 차이가 아주아주 작으면(예: 0.000000001보다 작으면) 같은 값으로 치자!"는 약속입니다.
	const epsilon = 1e-9 
	expected := 0.3
	diff := (a + b) - expected
	
	// diff(차이값)가 음수면 양수로 바꿔주는 작업 (절댓값)
	if diff < 0 {
		diff = -diff 
	}

	if diff < epsilon {
		fmt.Println("   [방법2] 차이가 거의 없으므로 0.3으로 인정!")
	}

	// 방법 3: 정수로 바꿔서 계산하기 (가장 정확!)
	// - 0.1과 0.2를 10배씩 곱해서 정수 1과 2로 만듭니다.
	// - 정수끼리는 오차 없이 완벽하게 더해집니다 (1+2=3).
	// - 마지막에 다시 10으로 나눠서 0.3을 만듭니다. (돈 계산할 때 주로 쓰는 방식)
	ai := int64(a*10 + 0.5) // 반올림 효과를 위해 0.5를 더함
	bi := int64(b*10 + 0.5) 
	result := float64(ai+bi) / 10 
	fmt.Printf("   [방법3] 정수로 변신시켜 계산: %v\n", result)
}

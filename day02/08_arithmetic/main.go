package main

import "fmt"

func main() {
	// [1. 기본 산술 연산자: 사칙연산]
	a, b := 17, 5

	fmt.Println("1. 기본 산술 연산 (a=17, b=5):")
	fmt.Println("   a + b =", a+b) 
	fmt.Println("   a - b =", a-b) 
	fmt.Println("   a * b =", a*b) 
	
	// [중요!] 정수와 정수를 나누면 결과도 무조건 '정수'입니다.
	// 17 나누기 5는 3.4지만, 소수점은 무참히 버려지고 3만 남습니다.
	fmt.Println("   a / b =", a/b)  // 결과: 3

	// [% 연산자]: '나머지'를 구합니다. 17을 5로 나눈 뒤 남은 2를 알려줍니다.
	// 주로 '홀수/짝수 판별'이나 '배수 확인'에 아주 많이 씁니다.
	fmt.Println("   a % b =", a%b)  // 결과: 2

	// [2. 실수 결과가 필요할 때]
	fmt.Println("\n2. 소수점 결과가 보고 싶을 때:")
	// 나누기 전에 숫자를 실수(float64)로 먼저 변신시켜야 소수점이 살아납니다.
	fmt.Printf("   float64(a)/float64(b) = %f\n", float64(a)/float64(b))

	// [3. 나머지 연산자의 부호]
	// 나머지의 부호는 무조건 '앞에 있는 숫자'를 따라갑니다.
	fmt.Println("\n3. 나머지(%) 부호 규칙:")
	fmt.Printf("   -7 %% 3 = %d (-7의 부호를 따름)\n", -7%3)

	// [4. 복합 대입 연산자: 줄여 쓰기]
	// x = x + 3 처럼 자기 자신에게 다시 값을 넣을 때 짧게 줄여 쓸 수 있습니다.
	fmt.Println("\n4. 복합 대입 연산자 (줄여 쓰기):")
	x := 10
	x += 3 // x = x + 3 과 같은 뜻입니다. (결과: 13)
	fmt.Printf("   x += 3  → x = %d\n", x)
	x *= 2 // x = x * 2 와 같은 뜻입니다. (결과: 26)
	fmt.Printf("   x *= 2  → x = %d\n", x)

	// [5. 증감 연산자: 하나씩 올리기/내리기]
	// n++: n에 1을 더합니다. / n--: n에서 1을 뺍니다.
	// 게임의 '레벨업'이나 '점수 추가' 등에서 아주 유용합니다.
	fmt.Println("\n5. 증감 연산자 (++ / --):")
	n := 5
	n++ // 5에서 1이 늘어나 6이 됨
	fmt.Printf("   n++ → n = %d\n", n)
	n-- // 6에서 다시 1이 줄어 5가 됨
	fmt.Printf("   n-- → n = %d\n", n)

	// [6. 연산자 우선순위]
	// 우리가 수학 시간에 배운 것처럼 곱하기(*), 나누기(/)가 더하기(+), 빼기(-)보다 먼저 계산됩니다.
	// 헷갈릴 때는 무조건 괄호 ( )를 쓰면 안전합니다!
	fmt.Println("\n6. 연산자 우선순위 (수학과 동일):")
	fmt.Printf("   2 + 3*4    = %d (3*4를 먼저 계산함)\n", 2+3*4)
	fmt.Printf("   (2+3)*4   = %d (괄호를 먼저 계산함)\n", (2+3)*4)
}
